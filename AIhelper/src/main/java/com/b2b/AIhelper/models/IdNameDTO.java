package com.b2b.AIhelper.models;

public class IdNameDTO {
    private String id;
    private String name;
    // constructor, getters, setters
    public IdNameDTO(String id, String name) {
        this.id = id;
        this.name = name;
    }
    public String getId() { return id; }
    public String getName() { return name; }
}

